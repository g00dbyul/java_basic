package example1.class_example;

public class Calculator {
    void powerOn() {
        System.out.println("Power on");
    }

    int plus(int a, int b) {
        return a + b;
    }

    double divide(int a, int b) {
        return (double) a / b;
    }

    void powerOff() {
        System.out.println("Power off");
    }
}
