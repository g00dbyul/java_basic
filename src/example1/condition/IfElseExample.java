package example1.condition;

public class IfElseExample {
    public static void main(String[] args) {
        int score = 85;

        if (score >= 90) {
            System.out.println(score);
            System.out.println("Grade : A");
        } else {
            System.out.println(score);
            System.out.println("Grade : B");
        }
    }
}
